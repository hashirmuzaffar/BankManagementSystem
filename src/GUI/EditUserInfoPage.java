    private void saveBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveBtnActionPerformed
        // TODO add your handling code here:
        User user = StorageManager.getCurrentUser();
        //         && !pinCodeField.getText().isEmpty() pther check
        if(user != null){
            if(!firstNameField.getText().trim().isEmpty() && !lastNameField.getText().trim().isEmpty()){
                user.setFirstName(firstNameField.getText());
                user.setLastName(lastNameField.getText());
                user.setPincode(pinCodeField.getText());
                
        File log = new File(user.getAccountNumber());//name of file
        try {
          
            writeObjectToFile(user,log);
        } catch (IOException ex) {
            Logger.getLogger(EditUserInfoPage.class.getName()).log(Level.SEVERE, null, ex);
            //Logger.getLogger(NewAccount.class.getName()).log(Level.SEVERE, null, ex);
        }
            }
            else{
                errorLabel.setText("Please enter all fields");
            }
        }
        else{
            System.out.println("user was null");
        }
        this.setVisible(false);
        new DisplayUserReport().setVisible(true);
    }//GEN-LAST:event_saveBtnActionPerformed

