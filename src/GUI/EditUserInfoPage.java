    public void displayData(){
        try {
//            String[] titles = {"Account Number", "Branch", "Account Type", "First Name", "Last Name", "Balance"};
            displayedTransactions = StorageManager.getInstance().getUserByAccountNumber(accountNumberDisplay.getText()).getTransaction();
//            2D array of String size=displayedUser.size()
//for loop insert in rows elements = 
            printTable(displayedTransactions);
        } catch (Exception ex) {
            Logger.getLogger(DisplayUserReport.class.getName()).log(Level.SEVERE, null, ex);
        }        
    }
    
public void printTable(ArrayList<Transaction> displayedTransactions){
    String[][] displayGrid = new String[displayedTransactions.size()][6];
            for(int i = 0; i<displayedTransactions.size(); i++){
                displayGrid[i] = displayedTransactions.get(i).getDisplayRow();
            }
            transactionTable.setModel(new javax.swing.table.DefaultTableModel(
            displayGrid,
            new String [] {
                "Date", "Transaction Type", "Amount", "Description"
            }
        ));
}
