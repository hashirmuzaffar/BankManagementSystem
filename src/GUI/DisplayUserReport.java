    private void filterBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_filterBtnActionPerformed
        try {
            // TODO add your handling code here:
            if((!branchCodeComboBox.getSelectedItem().equals("Any")) && accountTypeComboBox.getSelectedItem().equals("Any")) {
                displayedUsers = StorageManager.getInstance().getAllUsersByBranchCode(branchCodeComboBox.getSelectedItem().toString());
            }
            else{
            displayedUsers = StorageManager.getInstance().getAllUsersByBranchCodeAndAccountTypes(branchCodeComboBox.getSelectedItem().toString(), accountTypeComboBox.getSelectedItem().toString());
            }
            printTable(displayedUsers);

        } catch (Exception ex) {
            Logger.getLogger(DisplayUserReport.class.getName()).log(Level.SEVERE, null, ex);
        }
//        displayData();

    }//GEN-LAST:event_filterBtnActionPerformed

