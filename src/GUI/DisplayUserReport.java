    public void displayData() {
        String[] displayBranchesAndCities = StorageManager.getInstance().getBranchesAndCities();
        String[] displayAccountTypes = StorageManager.getInstance().getAccountTypes();
        branchCodeComboBox.setModel(new javax.swing.DefaultComboBoxModel<>(displayBranchesAndCities));
        accountTypeComboBox.setModel(new javax.swing.DefaultComboBoxModel<>(displayAccountTypes));
        try {
//            String[] titles = {"Account Number", "Branch", "Account Type", "First Name", "Last Name", "Balance"};
            displayedUsers = StorageManager.getInstance().getAllUsers();
//            2D array of String size=displayedUser.size()
//for loop insert in rows elements = 
            printTable(displayedUsers);
        } catch (Exception ex) {
            Logger.getLogger(DisplayUserReport.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }
public void printTable(ArrayList<User> displayedUsers){
    String[][] displayGrid = new String[displayedUsers.size()][6];
            for(int i = 0; i<displayedUsers.size(); i++){
                displayGrid[i] = displayedUsers.get(i).getDisplayRow();
            }
            usersTable.setModel(new javax.swing.table.DefaultTableModel(
            displayGrid,
            new String [] {
                "Account Number", "Branch", "Account Type", "First Name", "Last Name", "Balance"
            }
        ));
}
